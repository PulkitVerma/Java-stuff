public class Multiplier
{
	public static void main(String[] args)
	{
		int first_num = Integer.parseInt(args[0]);
		int snd_num = Integer.parseInt(args[1]);
		int trd_num = Integer.parseInt(args[2]);
		System.out.println(first_num + " * " + snd_num + " * " + trd_num + " = " + first_num*snd_num*trd_num);
	}
}